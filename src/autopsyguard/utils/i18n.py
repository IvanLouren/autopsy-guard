def resolve_language(config: MonitorConfig) -> str:
    """Resolve active language from config + OS locale."""
    raw = (getattr(config, "language", "auto") or "auto").strip().lower()
    if raw in {"pt", "en"}:
        return raw
    loc = ""
    try:
        default_loc = locale.getlocale()[0] or ""
        if not default_loc:
            default_loc = locale.getdefaultlocale()[0] or ""
        loc = default_loc.lower()
    except Exception:
        loc = ""
    return "pt" if loc.startswith("pt") else "en"


def tr(config: MonitorConfig, key: str, **kwargs: Any) -> str:
    """Translate message key using resolved language."""
    lang = resolve_language(config)
    msg = _MESSAGES.get(lang, _MESSAGES["en"]).get(key)
    if msg is None:
        msg = _MESSAGES["en"].get(key, key)
    if kwargs:
        try:
            return msg.format(**kwargs)
        except Exception:
            return msg
    return msg
